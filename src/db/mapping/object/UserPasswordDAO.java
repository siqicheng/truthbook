package db.mapping.object;

import db.mapping.baseDAO.BaseHibernateDAO;
import java.util.List;
import org.hibernate.LockMode;
import org.hibernate.Query;
import org.hibernate.criterion.Example;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

/**
 	* A data access object (DAO) providing persistence and search support for UserPassword entities.
 			* Transaction control of the save(), update() and delete() operations 
		can directly support Spring container-managed transactions or they can be augmented	to handle user-managed Spring transactions. 
		Each of these methods provides additional information for how to configure it for the desired type of transaction control. 	
	 * @see db.mapping.object.UserPassword
  * @author MyEclipse Persistence Tools 
 */

public class UserPasswordDAO extends BaseHibernateDAO  {
	     private static final Logger log = LoggerFactory.getLogger(UserPasswordDAO.class);
		//property constants



    
    public void save(UserPassword transientInstance) {
        log.debug("saving UserPassword instance");
        try {
            getSession().save(transientInstance);
            log.debug("save successful");
        } catch (RuntimeException re) {
            log.error("save failed", re);
            throw re;
        }
    }
    
	public void delete(UserPassword persistentInstance) {
        log.debug("deleting UserPassword instance");
        try {
            getSession().delete(persistentInstance);
            log.debug("delete successful");
        } catch (RuntimeException re) {
            log.error("delete failed", re);
            throw re;
        }
    }
    
    public UserPassword findById( db.mapping.object.UserPasswordId id) {
        log.debug("getting UserPassword instance with id: " + id);
        try {
            UserPassword instance = (UserPassword) getSession()
                    .get("db.mapping.object.UserPassword", id);
            return instance;
        } catch (RuntimeException re) {
            log.error("get failed", re);
            throw re;
        }
    }
    
    
    public List findByExample(UserPassword instance) {
        log.debug("finding UserPassword instance by example");
        try {
            List results = getSession()
                    .createCriteria("db.mapping.object.UserPassword")
                    .add(Example.create(instance))
            .list();
            log.debug("find by example successful, result size: " + results.size());
            return results;
        } catch (RuntimeException re) {
            log.error("find by example failed", re);
            throw re;
        }
    }    
    
    public List findByProperty(String propertyName, Object value) {
      log.debug("finding UserPassword instance with property: " + propertyName
            + ", value: " + value);
      try {
         String queryString = "from UserPassword as model where model." 
         						+ propertyName + "= ?";
         Query queryObject = getSession().createQuery(queryString);
		 queryObject.setParameter(0, value);
		 return queryObject.list();
      } catch (RuntimeException re) {
         log.error("find by property name failed", re);
         throw re;
      }
	}


	public List findAll() {
		log.debug("finding all UserPassword instances");
		try {
			String queryString = "from UserPassword";
	         Query queryObject = getSession().createQuery(queryString);
			 return queryObject.list();
		} catch (RuntimeException re) {
			log.error("find all failed", re);
			throw re;
		}
	}
	
    public UserPassword merge(UserPassword detachedInstance) {
        log.debug("merging UserPassword instance");
        try {
            UserPassword result = (UserPassword) getSession()
                    .merge(detachedInstance);
            log.debug("merge successful");
            return result;
        } catch (RuntimeException re) {
            log.error("merge failed", re);
            throw re;
        }
    }

    public void attachDirty(UserPassword instance) {
        log.debug("attaching dirty UserPassword instance");
        try {
            getSession().saveOrUpdate(instance);
            log.debug("attach successful");
        } catch (RuntimeException re) {
            log.error("attach failed", re);
            throw re;
        }
    }
    
    public void attachClean(UserPassword instance) {
        log.debug("attaching clean UserPassword instance");
        try {
            getSession().lock(instance, LockMode.NONE);
            log.debug("attach successful");
        } catch (RuntimeException re) {
            log.error("attach failed", re);
            throw re;
        }
    }
}